package uk.co.cheem.vcshistory.config;

/**
 * The interface Config.
 */
public interface Config {

  /**
   * Gets url.
   *
   * @return the url
   */
  String getUrl();

  /**
   * Gets owner name.
   *
   * @return the owner name
   */
  String getOwnerName();

  /**
   * Gets repo name.
   *
   * @return the repo name
   */
  String getRepoName();

  /**
   * Gets token.
   *
   * @return the token
   */
  String getToken();
}
